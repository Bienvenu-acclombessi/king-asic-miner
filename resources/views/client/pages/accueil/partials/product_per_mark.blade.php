// Fonction pour générer le HTML d'un produit
function generateProductHTML(product) {
    const stars = Math.floor(product.rating);
    const hasHalfStar = product.rating % 1 !== 0;
    const emptyStars = 5 - Math.ceil(product.rating);

    let starsHTML = '';
    for (let i = 0; i < stars; i++) {
        starsHTML += '<i class="bi bi-star-fill"></i>';
    }
    if (hasHalfStar) {
        starsHTML += '<i class="bi bi-star-half"></i>';
    }
    for (let i = 0; i < emptyStars; i++) {
        starsHTML += '<i class="bi bi-star"></i>';
    }

    const badgeHTML = product.badge ?
        `<div class="position-absolute top-0 start-0">
            <span class="badge ${product.badgeClass}">${product.badge}</span>
        </div>` : '';

    const oldPriceHTML = product.oldPrice ?
        `<span class="text-decoration-line-through text-muted">${product.oldPrice}</span>` : '';

    return `
        <div class="col">
            <div class="card card-product">
                <div class="card-body">
                    <div class="text-center position-relative">
                        ${badgeHTML}
                        <a href="pages/shop-single.html">
                            <img src="${product.image}" alt="${product.name}" class="mb-3 img-fluid">
                        </a>
                        <div class="card-product-action">
                            <a href="#!" class="btn-action" data-bs-toggle="modal" data-bs-target="#quickViewModal">
                                <i class="bi bi-eye" data-bs-toggle="tooltip" data-bs-html="true" title="Quick View"></i>
                            </a>
                            <a href="pages/shop-wishlist.html" class="btn-action" data-bs-toggle="tooltip" data-bs-html="true" title="Wishlist">
                                <i class="bi bi-heart"></i>
                            </a>
                            <a href="#!" class="btn-action" data-bs-toggle="tooltip" data-bs-html="true" title="Compare">
                                <i class="bi bi-arrow-left-right"></i>
                            </a>
                        </div>
                    </div>
                    <div class="text-small mb-1">
                        <a href="#!" class="text-decoration-none text-muted">
                            <small>${product.category}</small>
                        </a>
                    </div>
                    <h2 class="fs-6">
                        <a href="pages/shop-single.html" class="text-inherit text-decoration-none">${product.name}</a>
                    </h2>
                    <div class="text-warning">
                        <small>${starsHTML}</small>
                        <span class="text-muted small">${product.rating} (${product.reviews})</span>
                    </div>
                    <div class="d-flex justify-content-between align-items-center mt-3">
                        <div>
                            <span class="text-dark">${product.price}</span>
                            ${oldPriceHTML}
                        </div>
                        <div>
                            <a href="#!" class="btn btn-primary btn-sm">
                                <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none"
                                    stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"
                                    class="feather feather-plus">
                                    <line x1="12" y1="5" x2="12" y2="19"></line>
                                    <line x1="5" y1="12" x2="19" y2="12"></line>
                                </svg> Add
                            </a>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    `;
}

// Fonction pour charger les produits d'une marque
function loadProducts(brand) {
    const container = document.getElementById('productsContainer');
    const products = productsByBrand[brand];

    if (!products) return;

    // Animation de sortie
    container.style.opacity = '0';
    container.style.transition = 'opacity 0.3s ease';

    setTimeout(() => {
        // Remplacer le contenu
        container.innerHTML = products.map(product => generateProductHTML(product)).join('');

        // Animation d'entrée
        container.style.opacity = '1';

        // Réinitialiser les tooltips Bootstrap si disponible
        if (typeof bootstrap !== 'undefined') {
            const tooltipTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="tooltip"]'));
            tooltipTriggerList.map(function (tooltipTriggerEl) {
                return new bootstrap.Tooltip(tooltipTriggerEl);
            });
        }
    }, 300);
}

// Gestion des clics sur les liens de marque
document.addEventListener('DOMContentLoaded', function() {
    const brandLinks = document.querySelectorAll('.brand-link');

    brandLinks.forEach(link => {
        link.addEventListener('click', function() {
            // Retirer la classe active de tous les liens
            brandLinks.forEach(lnk => lnk.classList.remove('active'));

            // Ajouter la classe active au lien cliqué
            this.classList.add('active');

            // Charger les produits de la marque
            const brand = this.getAttribute('data-brand');
            loadProducts(brand);
        });
    });

    // Charger les produits de la première marque par défaut
    loadProducts('antinoid');
});
</script>